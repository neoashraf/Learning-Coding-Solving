/// It checks if the input is  a power of 2.

/// Explanation :
/// A 2^k has a 1 in kth position rest (k-1 to 0) are zero. So 2^k - 1 will have kth bit as 0 and (k-1 to 0) as 1.
/// The (x & x-1) operation yields to 0.

/// Examples :
/// 16 = 10000 & operation
/// 15 = 01111
/// 0  = 00000

int main() {
    int x,po;
    read(x);
    po = log2(x);
    if( ( x & (x-1) )== 0 ){
        cout<<"Yes."<<endl;
        cout<<x<<" = 2^"<<po<<"."<<endl;
    }
    else
        cout<<"It's not."<<endl;
    return 0;
}
